    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {


        $data = $request->validate([
            'description' => 'string',
            'image' => 'file'
        ]);

        $file = $request->file('image');
        $path = $file->store('images', 'private');

        Post::create([
            'description' => $data['description'],
            'user_id' => 1,
            'image_path' => $path
        ]);

        return redirect('/');
    }

